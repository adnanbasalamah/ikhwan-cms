<h2></h2>
<h1>Global Ikhwan di Perancis</h1>
<div class="reporter">Zefri Yazid : GI Jawa 2</div>
<p><img src="./mobilepage_files/mstory-jetdlm.jpg" width="200">
<strong>ikhwannews - Perancis,</strong>
Global Ikhwan Sdn Bhd cawangan Perancis telah menyertai Ekspo Perdagangan Islam sedunia di Le Bourget yang dianjurkan oleh Kesatuan Muslim Perancis dari 17 - 19 Disember, 2011. Pengunjung yang dianggarkan kira-kira 10 ribu orang terdiri dari pelbagai bangsa terutamanya orang Islam Perancis, Jerman, Belgium, United Kingdom, Sepanyol, Turki, Morocco, Algeria dan Tunisia. Sambutan yang diberikan adalah sangat menggalakkan. Dari ekspo tersebut kami telah mendapat beberapa kenalan baru yang berminat dengan Abuya dan jemaah.
<br><br>
Ada 6 wartawan dari stesen TV yang membuat liputan dan temubual iaitu dari WAFA TV, TV Assaqofi dan Oriental TV, Cordoba International TV, juga stesen TV dari Arab Saudi, Sepanyol, Belgium dan TV France Channel 24.  Sheikh Idris bercakap dalam bahasa Perancis memperkenalkan Abuya, jemaah, Generasi Harapan dan seterusnya barang-barang yang dijual.
Antara bahan yang dipamerkan untuk jualan ialah  buku-buku Islam untuk kanak-kanak seperti panduan solat, kisah-kisah Nabi dan mengenal Al-Quran. Ada juga dijual CD Islamik, baju-baju wanita untuk musim sejuk, serta key-chain dan poskad pemandangan indah Malaysia. Sepanjang ekspo kami hiaskan dengan gambar Abuya, pertunjukkan slide gambar Abuya serta aktiviti jemaah dan video klip muzik dari album 'Power Kaabah'.
<br><br>
Antara respon masyarakat terhadap sepanjang ekspo dalam memperkenalkan Abuya  secara terang-terangan di Perancis ini, mereka mendedahkan betapa Yahudi menguasai sistem pendidikan yang melarang perempuan Islam menutup aurat, malah undang-undang tersebut disokong oleh ulama Islam terkenal iaitu Imam Yusuf Al Qardawi. Ianya membuktikan ulama sangat tunduk dengan Yahudi. Di samping itu jelas di kalangan anak muda Islam mereka tidak mengenal huruf Al-Quran.
<br><br>
Benarlah Abuya pemimpin kasih sayang yang berani memperjuangkan Islam, tentunya kehadiran GISB di Perancis sangat-sangat diperlukan untuk memperkenalkan Islam sebenar.
Sekian, GISB Perancis
<br><br>
</p>
